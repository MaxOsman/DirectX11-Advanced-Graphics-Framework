void CubeGameObject::draw(ID3D11DeviceContext* pContext)
{
	pContext->PSSetShaderResources(1, 1, &m_pNormalTexture);
	pContext->GSSetShaderResources(2, 1, &m_pParallaxTexture);
	pContext->PSSetShaderResources(2, 1, &m_pParallaxTexture);

	draw(pContext, m_pTextureResourceView);
}

void CubeGameObject::draw(ID3D11DeviceContext* pContext, ID3D11ShaderResourceView* texture)
{
	// Set vertex buffer
	UINT stride = sizeof(SimpleVertex);
	UINT offset = 0;
	pContext->IASetVertexBuffers(0, 1, &m_pVertexBuffer, &stride, &offset);

	// Set index buffer
	//pContext->IASetIndexBuffer(m_pIndexBuffer, DXGI_FORMAT_R16_UINT, 0);

	//pContext->IASetPrimitiveTopology(D3D11_PRIMITIVE_TOPOLOGY_TRIANGLELIST);
	pContext->IASetPrimitiveTopology(D3D11_PRIMITIVE_TOPOLOGY_3_CONTROL_POINT_PATCHLIST);

	pContext->PSSetShaderResources(0, 1, &texture);
	pContext->PSSetSamplers(0, 1, &m_pSamplerLinear);

	pContext->Draw(NUM_VERTICES, 0);
}